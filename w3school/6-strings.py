#!/bin/python3

a = " Hello Aarif! "

print(a) # it is going to just print a

print(a.strip()) # it will remove any whitespace in front and end while defining variable

print(a[1]) # it will print characters position 1, it always starts from 0

print(a[1:5]) # it will print character from 1 till 5

print(a.lower())  # everything will be lower case

print(a.upper())  # everything will be upper case

print(len(a))   # it will print the length of a

print(a.split()) # it will split into substrings

print(a.replace("Aarif", "April")) # it will replace Aarif with April